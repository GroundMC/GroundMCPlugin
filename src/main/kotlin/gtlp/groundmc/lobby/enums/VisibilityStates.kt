package gtlp.groundmc.lobby.enums

/**
 * Enum to hold the visibility states.
 */
enum class VisibilityStates {
    /**
     * Hides all players.
     */
    NONE,

    /**
     * Hides all players except for friends.
     */
    FRIENDS,

    /**
     * Hides no players == All players are visible.
     */
    ALL;
}
